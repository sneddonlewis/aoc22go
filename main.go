package main

import (
	"fmt"
	"strings"
)

type outcome int
type weapon int

const (
	lose     outcome = 0
	draw     outcome = 3
	win      outcome = 6
	rock     weapon  = 1
	paper    weapon  = 2
	scissors weapon  = 3
)

type round struct {
	opponent weapon
	you      weapon
}

func NewRound(play string) *round {
	parts := strings.Split(play, " ")
	var opponent weapon
	switch parts[0] {
	case "A":
		opponent = rock
	case "B":
		opponent = paper
	case "C":
		opponent = scissors
	}
	var youTemp weapon
	switch parts[1] {
	case "X":
		youTemp = rock
	case "Y":
		youTemp = paper
	case "Z":
		youTemp = scissors
	}
	return &round{
		opponent: opponent,
		you:      youTemp,
	}
}

func (r *round) calculateOutcome() outcome {
	if r.you == rock {
		switch r.opponent {
		case rock:
			return draw
		case paper:
			return lose
		case scissors:
			return win
		}
	}
	if r.you == paper {
		switch r.opponent {
		case rock:
			return win
		case paper:
			return draw
		case scissors:
			return lose
		}
	}
	if r.you == scissors {
		switch r.opponent {
		case rock:
			return win
		case paper:
			return lose
		case scissors:
			return draw
		}
	}
	panic("expected unreachable code, invalid weapon")
}

func (r *round) score() (total int) {
	total += int(r.you)
	total += int(r.calculateOutcome())
	return total
}

func solve(plays []string) {
	var score int
	for _, v := range plays {
		r := NewRound(v)
		score += r.score()
	}
	fmt.Printf("Total score : %d\n", score)
}

